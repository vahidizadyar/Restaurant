    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $gallery = new Gallery($request->all());
        $imageName = time() . '.' . $request->file('file')->getClientOriginalExtension();
        $request->file('file')->move(public_path('/images/gallery'), $imageName);
        $gallery->photo = $imageName;
        $gallery->save();
        return redirect()->route('gallery.index')->withStatus('Slide successfully created.');
    }

